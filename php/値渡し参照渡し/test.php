<?php


//--------------------------------
// 通常の代入
//--------------------------------
$a = $b = "";

$a = $b;

$b = "str";

println("a=>$a");
println("b=>$b");

//------------------------
// 参照の代入
//------------------------
$a = $b = "";

$a = &$b;

$b = "str";

println("a=>$a");
println("b=>$b");


function println($str){
    echo $str.PHP_EOL;
}

?>
